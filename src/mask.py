
# ==================================================================================================

class Mask:

    # ----------------------------------------------------------------------------------------------

    def __init__(self, n=16):
        """
        Constructor.

        :param n: count of elements
        """

        self.N = 16
        self.E = [False] * self.N

    # ----------------------------------------------------------------------------------------------

    def __repr__(self):
        """
        Convert mask to string.

        :return: string
        """

        s = ['01'[e] for e in self.E]

        return 'mask-{0:2}:[{1}]'.format(self.N, ''.join(s))

